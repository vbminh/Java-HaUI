    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if("".equals(txtMaSV.getText()) || "".equals(txtHoTen.getText())){
             JOptionPane.showConfirmDialog(this,"Chua du thong tin de them","Thong Bao", WIDTH);
        }else{
            
        txtDiemTB.setText(sv.getDiemtb()+"");
        txtKetQua.setText(sv.getKetqua()+"");
        String masv = txtMaSV.getText();
        String hoten = txtHoTen.getText();
        String malop = (String) cbMaLop.getSelectedItem();
        float diem1 = Float.parseFloat(txtDiem1.getText());
        float diem2 = Float.parseFloat(txtDiem2.getText());
        float diemtb = Float.parseFloat(txtDiemTB.getText());
        String ketqua = txtKetQua.getText();
        
      
      
          sv = new SinhVienDTO(masv, hoten, malop, diem1, diem2, diemtb, ketqua);
          dsSV.add(sv);
          loadTable();
          
          
          //Them xong thi clear text
        txtMaSV.setText("");
        txtHoTen.setText("");
        cbMaLop.setSelectedItem("");
        txtDiem1.setText("");
        txtDiem2.setText("");
        txtDiemTB.setText("");
        txtKetQua.setText("");
        
        }
       

    }//GEN-LAST:event_btnThemActionPerformed

    private void tblSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSinhVienMouseClicked
        // TODO add your handling code here:
        dong = tblSinhVien.getSelectedRow();
        if(dong != -1){
        sv = dsSV.get(dong);
        txtMaSV.setText(sv.getMasv()+"");
        txtHoTen.setText(sv.getHoten()+"");
        cbMaLop.setSelectedItem(sv.getMalop());
        txtDiem1.setText(sv.getDiem1()+"");
        txtDiem2.setText(sv.getDiem2()+"");
        txtDiemTB.setText(sv.getDiemtb()+"");
        txtKetQua.setText(sv.getKetqua()+"");
        }

    }//GEN-LAST:event_tblSinhVienMouseClicked


    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        dong = tblSinhVien.getSelectedRow();
        if(dong !=-1){
            
        String masv = txtMaSV.getText()+"";
        String hoten = txtHoTen.getText()+"";
        String malop = cbMaLop.getSelectedItem().toString();
        float diem1 = Float.parseFloat(txtDiem1.getText()+"");
        float diem2 = Float.parseFloat(txtDiem2.getText()+"");
        float diemtb = Float.parseFloat(txtDiemTB.getText()+"");
        String kq = txtKetQua.getText()+"";
        
        
           SinhVienDTO svNew = new SinhVienDTO(masv, hoten, malop,diem1,diem2,diemtb,kq);
           dsSV.set(dong,svNew);
          loadTable();
        }else{
            JOptionPane.showConfirmDialog(this,"Chua chon dong xoa","Thong Bao", WIDTH);
        }
        
        
        //BingDing
          DefaultTableModel model = (DefaultTableModel) tblSinhVien.getModel();
        txtMaSV.setText(model.getValueAt(tblSinhVien.getSelectedRow(),0).toString());
          txtHoTen.setText(model.getValueAt(tblSinhVien.getSelectedRow(),1).toString());
          cbMaLop.setSelectedItem(model.getValueAt(tblSinhVien.getSelectedRow(),2).toString());
          txtDiem1.setText(model.getValueAt(tblSinhVien.getSelectedRow(),3).toString());
          txtDiem2.setText(model.getValueAt(tblSinhVien.getSelectedRow(),4).toString());
          txtDiemTB.setText(model.getValueAt(tblSinhVien.getSelectedRow(),5).toString());
          txtKetQua.setText(model.getValueAt(tblSinhVien.getSelectedRow(),6).toString());


    }//GEN-LAST:event_btnSuaActionPerformed

